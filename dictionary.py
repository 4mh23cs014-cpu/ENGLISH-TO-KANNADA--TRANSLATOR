def get_word_translation(word):
    """Get translation of a single word."""
    word_lower = word.lower().strip()
    return ENGLISH_TO_KANNADA.get(word_lower, None)

def get_all_translations(word):
    """Get all possible translations for a word (case-insensitive)."""
    word_lower = word.lower().strip()
    matches = {k: v for k, v in ENGLISH_TO_KANNADA.items() if k == word_lower}
    return matches
